import UIKit

extension UIFont {
    static var appBlack: String {
        get {
            return "Mulish-Black"
        }
    }
    
    static var appBold: String {
        get {
            return "Mulish-Bold"
        }
    }
    
    static var appSemibold: String {
        get {
            return "Mulish-SemiBold"
        }
    }
    
    static var appMedium: String {
        get {
            return "Mulish-Medium"
        }
    }
}
